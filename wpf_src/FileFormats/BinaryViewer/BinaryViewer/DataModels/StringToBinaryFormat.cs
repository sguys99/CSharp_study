namespace BinaryViewer
{
    /// <summary>
    /// The format to use when converting strings to binary
    /// </summary>
    public enum StringToBinaryFormat
    {
        /// <summary>
        /// ASCII format
        /// </summary>
        ASCII,
        /// <summary>
        /// UTF7 unicode format
        /// </summary>
        UTF7,
        /// <summary>
        /// UTF8 unicode format
        /// </summary>
        UTF8,
        /// <summary>
        /// UTF32 unicode format
        /// </summary>
        UTF32,
        /// <summary>
        /// Unicode format (USC2)
        /// </summary>
        Unicode
    }
}
